#include <iostream>
#include <cstdio>
#include <cmath>

long long x;
long long b[] = {9, 180, 2700, 36000, 450000, 5400000, 63000000, 720000000, 8100000000, 90000000000,
990000000000, 10800000000000, 117000000000000, 1260000000000000, 13500000000000000,
144000000000000000, 1530000000000000000};
long long d[] = {1, 10, 100, 1000, 10000, 100000, 1000000, 10000000, 100000000, 1000000000, 10000000000,
100000000000, 1000000000000, 10000000000000, 100000000000000, 1000000000000000,
10000000000000000, 100000000000000000, 1000000000000000000};

int main()
{
    scanf("%lld", &x);
    long long sum = 0;
    int i;
    for(i = 0; sum < x; ++i)
        sum += b[i];
    sum -= b[--i];
    x -= sum + 1;
    printf("%d\n", ((d[i] + x / (i + 1)) / d[i - (x % (i + 1))]) % 10);
    return 0;
}
